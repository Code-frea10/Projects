public class Variablesjava {
    public static void main(String[] args) {
        // variables
        // variables are container to store data for temporary used
        byte age = 20;
        int number = 10;
        float myFloatingNum = 3.14f;
        double price = 123_456_789d;
        char section = 'A';
        String name = "John";

        System.out.println(age);
        System.out.println(number);
        System.out.println(myFloatingNum);
        System.out.println(price);
        System.out.println(section);
        System.out.println(name.toUpperCase());
    }
}
